//   を統合し、顧客に会ったこともない主人公を“第一号のFDE”としてフラッグシップ顧客・
//   カルゴ物流へ送り込む。そこでは StockPilotの在庫最適化機能を情シスが鳴り物入りで
//   入れたのに、現場では誰も使っていない。解約は目前。このロゴを失えばルーメンの転換は
//   頓挫し、次はリストラだ。——沈黙する基幹システムを、現場の言葉でもう一度動かせるか。
//
// ■ 企業の階層（背景設定。詳細は docs/STORY.md §1）: カルゴ物流は元は独立の中堅物流。総本山
//   ジェネリック電機（総合電機・産業機器の大手。フィジカルAIへ転換し苦戦、政府系ファンド支援下）が
//   “フィジカルAIの実証の場”として買収。だが現場はシステムを入れても使われず実態がデータ化されておらず実証は進まず、グループは数字を
//   取り繕うため買収したカルゴ物流を架空利益計上（粉飾）に使う。親は ジェネリックロジスティクス。
//   主人公の会社ルーメンはカルゴ物流と契約（FDEはその契約のもとで現場のIT化・自動化から着手）。
//   上層は「本物の成果」より「見栄えの数字」を欲する＝既存テーマ「数字を盛る vs 正直」の出どころ。
//   現状の第1章本文は現場に閉じ、この階層・粉飾は背景（出すときは平易に補足・実在の企業/事件を断定しない）。
//
// ■ 3幕（既存 SPRINTS に接続）
//   第1幕 Sprint1「現場を知る」: なぜ使われないのか。沈黙と不信の底。属人化した
//     ボトルネック（橋本）が見えてくる＝制約理論的な発見。
//   第2幕 Sprint2「仮説を形にする」: 誤出荷を減らす最小版を出して学ぶ。割り込み・DoD・
//     AI過信などの試練。中盤に大障害が走る（フィードバックの一way）。
//   第3幕 Sprint3「文化を残す」: 本番化と文化定着。AIモデル退化の山場（過信した周回で
//     爆発）。継続的学習でフェニックスが飛び立つ／墜ちる。
//
// ■ 主軸の分岐（決定論・背骨に載せる）: 現場は自動化を恐れてノウハウ（例外処理）を隠す。
//   Sprint2 レトロ(s2-retro)で「信頼を築く(genbaTrust)」か「トップダウンで乗り切る(topDown)」を
//   毎回選ぶ。Sprint3 のレビュー/レトロは requiresFlag バリアントで経路ごとに分岐する
//   （信頼=ノウハウ開示→定着／トップダウン=隠れた例外が漏れ大障害→挽回の余地）。
// ───────────────────────────────────────────────────────────

